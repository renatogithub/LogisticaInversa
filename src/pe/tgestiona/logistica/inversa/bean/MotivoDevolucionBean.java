package pe.tgestiona.logistica.inversa.bean;

public class MotivoDevolucionBean {
	private String codDevol;
	private String nomDevol;
	private String tipoFormato;
	private String codTipoFormato;
	private String abrvMotivo;
	
	public MotivoDevolucionBean() {
		super();
	}
	public String getCodDevol() {
		return codDevol;
	}
	public void setCodDevol(String codDevol) {
		this.codDevol = codDevol;
	}
	public String getNomDevol() {
		return nomDevol;
	}
	public void setNomDevol(String nomDevol) {
		this.nomDevol = nomDevol;
	}
	public String getTipoFormato() {
		return tipoFormato;
	}
	public void setTipoFormato(String tipoFormato) {
		this.tipoFormato = tipoFormato;
	}
	public String getCodTipoFormato() {
		return codTipoFormato;
	}
	public void setCodTipoFormato(String codTipoFormato) {
		this.codTipoFormato = codTipoFormato;
	}
	public String getAbrvMotivo() {
		return abrvMotivo;
	}
	public void setAbrvMotivo(String abrvMotivo) {
		this.abrvMotivo = abrvMotivo;
	}
	
	
	
	
	
	
	
	
}
