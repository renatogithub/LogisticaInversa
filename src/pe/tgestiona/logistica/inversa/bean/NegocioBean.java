package pe.tgestiona.logistica.inversa.bean;

public class NegocioBean {
	private String codNegocio;
	private String nomNegocio;
	
	public NegocioBean() {
		super();
	}
	
	public String getCodNegocio() {
		return codNegocio;
	}
	
	public void setCodNegocio(String codNegocio) {
		this.codNegocio = codNegocio;
	}
	
	public String getNomNegocio() {
		return nomNegocio;
	}
	
	public void setNomNegocio(String nomNegocio) {
		this.nomNegocio = nomNegocio;
	}
	
}
