package pe.tgestiona.logistica.inversa.bean;

public class TecnologiaMaterialBean {
	private String codTecnologia;
	private String nomTecnologia;
	
	public TecnologiaMaterialBean() {
		super();
	}

	public String getCodTecnologia() {
		return codTecnologia;
	}

	public void setCodTecnologia(String codTecnologia) {
		this.codTecnologia = codTecnologia;
	}

	public String getNomTecnologia() {
		return nomTecnologia;
	}

	public void setNomTecnologia(String nomTecnologia) {
		this.nomTecnologia = nomTecnologia;
	}
	
}
