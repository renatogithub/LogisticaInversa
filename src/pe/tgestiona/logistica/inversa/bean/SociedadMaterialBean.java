package pe.tgestiona.logistica.inversa.bean;

public class SociedadMaterialBean {
	private String codSociedad;
	private String nomSociedad;
	private String abrvSociedad;
	
	public SociedadMaterialBean() {
		super();
	}

	public String getCodSociedad() {
		return codSociedad;
	}

	public void setCodSociedad(String codSociedad) {
		this.codSociedad = codSociedad;
	}

	public String getNomSociedad() {
		return nomSociedad;
	}

	public void setNomSociedad(String nomSociedad) {
		this.nomSociedad = nomSociedad;
	}

	public String getAbrvSociedad() {
		return abrvSociedad;
	}

	public void setAbrvSociedad(String abrvSociedad) {
		this.abrvSociedad = abrvSociedad;
	}
	
	
	
	
}
