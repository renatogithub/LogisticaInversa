package pe.tgestiona.logistica.inversa.bean;

public class RubroMaterialBean {
	private String codRubro;
	private String nomRubro;
	private String abrRubro;
	
	public RubroMaterialBean() {
		super();
	}
	public String getCodRubro() {
		return codRubro;
	}
	public void setCodRubro(String codRubro) {
		this.codRubro = codRubro;
	}
	public String getNomRubro() {
		return nomRubro;
	}
	public void setNomRubro(String nomRubro) {
		this.nomRubro = nomRubro;
	}
	public String getAbrRubro() {
		return abrRubro;
	}
	public void setAbrRubro(String abrRubro) {
		this.abrRubro = abrRubro;
	}
	
	
	
	
	
}
